# Напишіть програму на Python для розсилки кількох днів між двома датами. Дати вибору: (2014, 7, 2), (2014, 7, 11) . Очікуваний вихід: 9 днів.

from datetime import datetime

# Визначення початкової та кінцевої дат
start_date = datetime(2014, 7, 2)
end_date = datetime(2014, 7, 11)

# Обчислення різниці між датами
difference = end_date - start_date

# Виведення результату
print(f"Кількість днів між датами: {difference.days} днів")
