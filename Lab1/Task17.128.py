#17.128. Напишіть програму на Python для виведення однієї пари ключа-значення з слова в переменних.
# Створення словника
my_dict = {'ключ': 'значення', 'інший_ключ': 'інше_значення', 'ще_один_ключ': 'ще_одне_значення'}

# Виведення однієї пари ключ-значення у змінних
for key, value in my_dict.items():
    print("Ключ:", key)
    print("Значення:", value)
    break  # Вийти з циклу після першої пари
