#Напишіть програму на Python для розбиття рядка змінної
# довжини на літера-пробіл.

# Введення рядка змінної довжини
input_string = input("Введіть рядок: ")

# Розбивка рядка на літери та додавання пробілів
result_string = ' '.join(input_string)

# Виведення результату
print("Результат:", result_string)
