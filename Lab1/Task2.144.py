# Депозит складає 23% річних. Клієнт звернувся в банк через b місяців.
# Він отримає c грошей. Скільки грошей отримає клієнт?
def calculate_future_value(principal, rate, time_months):
    rate_decimal = rate / 100  # конвертуємо відсоткову ставку у десятковий вигляд
    n = 12  # кількість разів нарахування відсотків на рік
    t = time_months / 12  # кількість років
    future_value = principal * (1 + rate_decimal / n) ** (n * t)
    return future_value

# Вхідні дані
a = float(input("Введіть початкову суму грошей: "))
r = float(input("Введіть річний процент: "))
b = int(input("Введіть кількість місяців: "))

# Обчислення майбутньої вартості
c = calculate_future_value(a, r, b)

# Виведення результату
print(f"Клієнт отримає {c:.2f} грошей після {b} місяців.")
